# todo: Единицы массы пронумерованы следующим образом: 1 — килограмм, 2 — миллиграмм, 3 — грамм,
#  4 — тонна, 5 — центнер. Дан номер единицы массы и масса тела M в этих единицах (вещественное число).
#  Вывести массу данного тела в килограммах

number = int(input("Введите номер единицы массы (1-кг, 2-мг, 3-г, 4-т, 5-ц): "))
M = float(input("Введите массу тела: "))

if number == 1:
    result = M
elif number == 2:
    result = M / 1000000
elif number == 3:
    result = M / 1000
elif number == 4:
    result = M * 1000
elif number == 5:
    result = M * 100
else:
    print("Ошибка: введите номер единицы от 1 до 5!")
    result = None

if result is not None:
    print("Масса в килограммах:", result)